NCAD = data(:,1);
F    = data(:,2);

%% =====================================================
% 2. SEPARACIÓN DE RANGOS
%% =====================================================
idx1 = (NCAD <= 195);
idx2 = (NCAD >= 196);

NCAD1 = NCAD(idx1);  F1 = F(idx1);
NCAD2 = NCAD(idx2);  F2 = F(idx2);

%% =====================================================
% 3. AJUSTE POLINOMIAL CÚBICO
%% =====================================================
p1 = polyfit(NCAD1, F1, 3);
p2 = polyfit(NCAD2, F2, 3);

F1_hat = polyval(p1, NCAD1);
F2_hat = polyval(p2, NCAD2);

%% =====================================================
% 4. MÉTRICAS DE ERROR (SIN DIVISIÓN ENTRE CERO)
%% =====================================================
calc_metrics = @(F, Fh) struct( ...
    'R2', 1 - sum((F-Fh).^2)/sum((F-mean(F)).^2), ...
    'STD', std(F-Fh), ...
    'MAE', mean(abs(F-Fh)), ...
    'MAPE', mean(abs((F(F~=0)-Fh(F~=0))./F(F~=0)))*100, ...
    'MaxRelErr', max(abs((F(F~=0)-Fh(F~=0))./F(F~=0)))*100 ...
);

M1 = calc_metrics(F1, F1_hat);
M2 = calc_metrics(F2, F2_hat);

%% =====================================================
% 5. RESULTADOS
%% =====================================================
disp('===== 0 ≤ NCAD ≤ 195 =====');
disp('Polinomio cúbico:'); disp(p1);
disp(M1);

disp('===== 196 ≤ NCAD ≤ 1023 =====');
disp('Polinomio cúbico:'); disp(p2);
disp(M2);

